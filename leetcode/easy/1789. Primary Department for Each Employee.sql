SELECT
    X.employee_id,
    X.department_id
FROM(
    SELECT
        employee_id,
        department_id,
        primary_flag,
        COUNT(employee_id) OVER(PARTITION BY employee_id) AS emp_count
    FROM Employee
) X
WHERE X.primary_flag = 'Y' OR X.emp_count = 1